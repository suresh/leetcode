# @lc code=start
class Solution:
    def isValid(self, s: str) -> bool:

        bracket_map = {"(": ")", "[": "]",  "{": "}"}
        open_par = set(["(", "[", "{"])
        stack = []
        for i in s:
            if i in open_par:
                stack.append(i)
            elif stack and i == bracket_map[stack[-1]]:
                stack.pop()
            else:
                return False
        return stack == []
        
# @lc code=end


if __name__ == '__main__':
    s = Solution()
    s.isValid('([])')
